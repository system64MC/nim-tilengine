proc drawFilledTriangle*(bitmap: Bitmap,
                       p1, p2, p3: Point, color: uint8 = 1) =
  # Sort the points by y-coordinate
  var points: array[3, Point] = [p1, p2, p3]
  for i in 0..1:
    for j in 0..(1-i):
      if points[j].y > points[j+1].y:
        let temp = points[j]
        points[j] = points[j+1]
        points[j+1] = temp

  # Extract the sorted points
  let top = points[0]
  let middle = points[1]
  let bottom = points[2]

  # Calculate the slopes of the two edges of the triangle
  let invslope1 = (middle.x - top.x) div (middle.y - top.y)
  let invslope2 = (bottom.x - top.x) div (bottom.y - top.y)

  # Initialize the x-coordinates for the left and right edges of the triangle
  var x1 = top.x
  var x2 = top.x

  # Loop through each scanline from top to bottom of the triangle
  for scanlineY in top.y..bottom.y:
    # Draw a horizontal line between the two edges
    for x in min(x1, x2)..max(x1, x2):
      bitmap.getData(x, scanlineY)[0] = color

    # Update the x-coordinates for the left and right edges
    x1 += invslope1
    x2 += invslope2

