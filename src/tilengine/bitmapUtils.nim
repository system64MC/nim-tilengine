proc cubicInterpolation(p0, p1, p2, p3, t: float): float =
  let
    u = 1.0 - t
    u2 = u * u
    u3 = u2 * u
    t2 = t * t
    t3 = t2 * t
  result = u3 * p0 + 3 * u2 * t * p1 + 3 * u * t2 * p2 + t3 * p3

